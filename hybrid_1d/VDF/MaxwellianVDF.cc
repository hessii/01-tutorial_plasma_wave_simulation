namespace {
    constexpr H1D::Real quiet_nan = std::numeric_limits<H1D::Real>::quiet_NaN();
}

H1D::MaxwellianVDF::MaxwellianVDF() noexcept
: vth1(quiet_nan), T2OT1(quiet_nan) {
}
H1D::MaxwellianVDF::MaxwellianVDF(Real const vth1, Real const T2OT1)
: MaxwellianVDF() {
    if (vth1 <= 0) {
        std::invalid_argument(std::string(__FUNCTION__) + " - non-positive parallel thermal speed");
    }
    if (T2OT1 <= 0) {
        std::invalid_argument(std::string(__FUNCTION__) + " - non-positive temperature ratio");
    }
    this->vth1 = vth1;
    this->T2OT1 = T2OT1;
}

auto H1D::MaxwellianVDF::operator()() const
-> Particle {
    Particle ptl = load();
    // rescale
    ptl.vel *= vth1;
    ptl.pos_x *= Input::Nx; // [0, Nx)
    return ptl;
}
auto H1D::MaxwellianVDF::load() const
-> Particle {
    // position
    //
    Real const pos_x = uniform_real(); // [0, 1]

    // velocity in field-aligned frame (Hu et al., 2010, doi:10.1029/2009JA015158)
    //
    Real const phi = uniform_real()*2*M_PI; // [0, 2pi]
    Real const v1 = ierf(uniform_real())*(uniform_real() < .5 ? -1 : 1);
    Real v2 = std::sqrt(-std::log(uniform_real())*T2OT1);
    Real const v3 = std::sin(phi)*v2;
    v2 *= std::cos(phi);

    // velocity in Cartesian
    //
    H1D::Real const theta = Input::theta*M_PI/180; // degree to radian
    Real const vx = v1*std::cos(theta) - v2*std::sin(theta);
    Real const vy = v1*std::sin(theta) + v2*std::cos(theta);
    Real const vz = v3;

    return {Vector{vx, vy, vz}, pos_x};
}

auto H1D::MaxwellianVDF::ierf(Real x) noexcept
-> Real {
    x *= table.size();
    unsigned const i = static_cast<unsigned>(x); // [0, 5000]
    if (i + 1 >= table.size()) {
        return table.back();
    } else { // i = [0, 4998]; linear interpolation
        return table[i+1]*(x - i) + table[i+0]*(i + 1 - x);
    }
}

